void executeCmd(const uint8_t* cmd)
{
  uint32_t len = strlen((const char*)(cmd)) - 1;
  uint8_t help_prompt[]  = "This is help command.\r\r#";
  uint8_t error_prompt[] = "This is a wrong command.\r\r#";
  if (strncmp((const char*)(cmd), "help", len) == 0) {
    HAL_UART_Transmit(&huart1, help_prompt, strlen((const char*)(help_prompt)), HAL_MAX_DELAY);
  } else {
    HAL_UART_Transmit(&huart1, error_prompt, strlen((const char*)(error_prompt)), HAL_MAX_DELAY);
  }
}
